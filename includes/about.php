          <p>The initial construction of buildings and infrastructure inside the camp in 1976 was under the supervision of BGEN HERMINIO N AHORRÃ–, who was then the Commander of Task Force "Pagkakalsa," stationed at the Davao City International Airport in Barangay Sasa.</p>
          <br>  
          <p>Initially, there were only four buildings of the Command - The Regional Headquarters building, which houses the Command Group and the General Staff today, the Headquarters Service Company building, the Enlisted Personnel Barracks and the Headquarters Mess Hall. In 1979, thirteen more bulldings were constructed including the seven barracks of the former 56th PC Battallon. Most of these buildings were constructed by the Engineering Brigade of the Philippine Army, with the use of prefabricated steel materials. Years later, twenty-nine additional buildings were constructed including two guard houses at the main gate, a basketball court, two tennis courts and two condominium buildings for Police Commissioned Officers and Police Non-Commissioned Officers assigned in the Regional Police Headauarters.</p>
