          <h2>OUR STORY</h2>
          <p>On June 1. 1978. the former 4th PC Zone stationed at Camp Parang in Maguindanao was divided into four commands, which led to the creation of the Philippine Constabulary/Integrated National Police Regional Command XI pursuant to General Orders Number 126, HPC/INP dated 01 June 1978 with its assigned areas of responsibility covering the areas of Davao del Norte, Davao del Sur, Davao Oriental, Surigao del Sur, South Cotabato, Davao City and General Santos City.</p>
